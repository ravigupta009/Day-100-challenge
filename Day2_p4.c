#include<stdio.h>
int main(){
    int r = 7 , r1 = 3;

    printf("Area of circle = %f\n", 3.14 * r * r);
    printf("Circumference of circle = %f\n", 2 * 3.14 * r);
    printf("The next value of circle of area and circumference is:\n");
    printf("Area of circle = %f\n", 3.14 * r1 * r1);
    printf("Circumference of circle = %f\n", 2 * 3.14 * r1);
    return 0;
}